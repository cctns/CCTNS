           <p><h6>Hon'ble Union Home Minister mentioned the role of NCRB and CCTNS during the IB Endowment lecture at Vigyan
            Bhawan, New Delhi on 23/12/2009 in the following words. "The police stations in the country are, today,
            virtually unconnected islands. Thanks to telephones and wireless, and especially thanks to mobile telephones, 
            there is voice connectivity between the police station and senior police officers, but that is about all.
            There is no system of data storage, data sharing and accessing data.<br> 
            There is no system under which one police station can talk to another directly. There is no record of crimes or
            criminals that can be accessed by a Station House Officer, 
            except the manual records relating to that police station. 
            Realising the gross deficiency in connectivity, the Central government
            is implementing an ambitious scheme called "Crime and Criminal Tracking Network System (CCTNS)".
            The goals of the system are to facilitate collection, storage, retrieval, analysis, transfer and sharing of
            data and information at the police station and between the police station and the State Headquarters and the
            Central Police Organisations.</h6></p>
